int main()
{

    //For loop:
    // for(<instruction1>; <condition>; <instruction2>){...}
    //instruction 1 runs at the start of the loop
    //condition is checked at the beginning of each iteration
    //instruction 2 runs after each iteration
    for(int i = 0; i < 10; i++)
    {

    }

    //While loops
    //while(<condition>){..}
    //condition is checked at the start of each iteration
    while(1)
    {

    }

    //do while
    //runs at least one and then loops back if the condition within while is true
    do
    {

    }while(1);

    return 0;
}